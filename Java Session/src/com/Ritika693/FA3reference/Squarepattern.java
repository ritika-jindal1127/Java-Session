package com.Ritika693.FA3reference;

public class Squarepattern {
    public static void main(String[] args) {
        int rows=4;
        int cols=4;

        for(int i=0;i<=rows;i++)
        {
            for(int j=1;j<=cols;j++)
            {
                System.out.print("*");
            }
            System.out.println();
        }
    }
}
