package com.Ritika693.Day39;

public class Printhighlow {

        public static void main(String[] args) {
            int i = 1;  // starting number

            while (i <= 10) {  // loop runs from 1 to 10
                if (i > 5) {
                    System.out.println(i + " → High");
                } else {
                    System.out.println(i + " → Low");
                }
                i++;  // increment the counter
            }
        }
    }


