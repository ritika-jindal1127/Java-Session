package com.Ritika693.Day39;

public class OddNumbers {
    public static void main(String[] args) {
            System.out.println("Odd numbers between 1 and 30:");

            for (int i = 1; i <= 30; i++) {
                if (i % 2 != 0) {  // check if number is odd
                    System.out.println(i);
                }
            }
        }
    }


